package exercise;

public class TiposNumericos {
	public static void main(String[] args) {
		// Integers
		
		Byte numberA = 1; //-128 and 127 <- 8bits
		Short numberB = 1; // -32768 and 32767 <- 16bits
		Integer numberC = 1; // -(2^31) and (2^31)-1 <- 32bits
		Long numberD = 1L; // -(2^63) and (2^63)-1 <- 64bits
		
		// Decimals
		
		Float numberE = 1.0f;
		Double numberF = 1.0;
		
	}

}
