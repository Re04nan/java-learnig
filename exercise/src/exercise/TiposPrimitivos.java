package exercise;

public class TiposPrimitivos {

	public static void main(String[] args) {
		
		// Tipos primitivos não podem ter um estado de ausência de valores
		// enquanto os demais podem, a ausência é definida pelo valor null;
		
		byte numberA = 1; // Byte numberA = null;
		short numberB = 1; // Short numberB = null;
		int numberC = 1; // Integer numberC = null;
		long numberD = 1; // Long numberD = null;
		float numberE = 1.0f; // Float numberE = null;
		double numberF = 1.0; // Double numberF = null;
		char textA = 'Z'; // Character textA = null;
		boolean check = true; // Boolean check = null;
	}

}
